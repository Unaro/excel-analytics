/**
 * Правило нормализации ключа перед сопоставлением со справочником.
 *
 * Excel теряет ведущие нули числовых кодов (01512000106 → 1512000106),
 * поэтому и ключи справочника, и значения данных приводятся к единому
 * виду: lpad8/lpad11 дополняют числовой код нулями слева до 8/11 знаков.
 */
export type KeyNormalization = 'none' | 'lpad8' | 'lpad11';

/**
 * Пользовательский тип: связка «справочник + правила сопоставления».
 *
 * Группировка и фильтры продолжают работать по кодам — тип влияет
 * ТОЛЬКО на отображение (код → наименование) в дереве иерархии,
 * drill-down и таблицах.
 */
export interface ReferenceType {
  id: string;
  /** Имя типа, видимое в настройке колонки («ОКТМО», «ОКАТО»). */
  name: string;
  /** id датасета-справочника (role: 'reference'). */
  dictionaryDatasetId: string;
  /** Колонка справочника с кодом. */
  keyColumn: string;
  /** Колонка справочника с отображаемым наименованием. */
  displayColumn: string;
  keyNormalization: KeyNormalization;
  /** Размер построенного словаря (записей) — для UI. */
  entryCount?: number;
  createdAt: number;
  updatedAt: number;
}
