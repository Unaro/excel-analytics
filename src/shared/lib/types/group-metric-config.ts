// Контракт UI-конфигурации метрики группы. Живёт в shared,
// потому что используется сервисами экспорта/импорта конфигурации.
// ─────────────────────────────────────────────────────────────

import type { ColorConfig } from './dashboard';
import type { MetricChartStyle } from './chart';

/**
 * Конфигурация одной метрики внутри группы показателей.
 *
 * Хранится ОТДЕЛЬНО от IndicatorGroup, чтобы не смешивать
 * бизнес-логику (агрегации/формулы) с UI-настройками (цвета, пороги, стиль чарта).
 */
export interface GroupMetricConfig {
  colorConfig?: ColorConfig;
  /** Как метрика отображается на чарте «Столбцы»: столбец/линия + стиль линии. */
  chartStyle?: MetricChartStyle;
}
