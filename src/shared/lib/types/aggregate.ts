// shared/lib/types/aggregate.ts
// Тип узла файла-агрегата (мега-босс, фаза 2). Живёт в shared, чтобы и фича
// импорта (извлечение), и entity-стор (хранение) могли его использовать без
// нарушения границ FSD. План: docs/architecture/aggregate-files.md

export interface AggregateNode {
  /** Путь значений ключевых колонок от корня до узла. */
  path: string[];
  /** Уровень = глубина в каскаде (индекс самого правого ключа). */
  level: number;
  /** Метка узла (последний элемент пути). */
  label: string;
  /** Это строка «Итого/Всего»? */
  isTotal: boolean;
  /** Введённые/предпосчитанные значения метрик по составному имени колонки. */
  values: Record<string, number | null>;
}

/** Настройка трактовки пустоты (0 — НЕ пустота по умолчанию). */
export interface EmptyConfig {
  /** Доп. значения, считающиеся пустотой («—», «н/д», …). Регистр игнорируется. */
  tokens?: string[];
}

/**
 * Спецификация шаблона показателя, заданная пользователем при импорте агрегата
 * (формула + формат). Связь с колонками — по имени (см. `metricTemplateNames`).
 * Фаза 1 — только ОДНОВХОДОВЫЕ формулы: ровно один алиас поля (`SUM(value)`,
 * `AVG(value)`, `value/1000`), к которому привязывается колонка-метрика.
 */
export interface AggregateTemplateSpec {
  /** Имя логического показателя (= имя шаблона; ключ связи с колонками). */
  name: string;
  /** Формула (одновходовая): напр. `SUM(value)`, `AVG(value)`. */
  formula: string;
  /** Единственный алиас поля в формуле — к нему привязывается колонка. */
  alias: string;
  /** Формат отображения (значения DisplayFormat; строкой — граница FSD). */
  displayFormat: string;
  /** Знаков после запятой. */
  decimalPlaces: number;
  /** Единица измерения (необязательно). */
  unit?: string;
}

/**
 * Подтверждённая пользователем разметка файла-агрегата (сплющивание + узлы).
 * Сериализуема — хранится на датасете (для замены файла) и проходит через
 * экспорт/импорт конфигов.
 */
export interface AggregateLayoutConfig {
  headerRows: number;
  keyColumns: number[];
  empty?: EmptyConfig;
  totalKeywords?: string[];
  /** Группы шапки, которые НЕ создавать (имена как в proposeGroups). */
  excludeGroups?: string[];
  /**
   * Имя логического показателя (= шаблона) для колонки-метрики, по её составному
   * имени (fullName). Колонки с ОДИНАКОВЫМ именем → общий шаблон. Нет записи →
   * имя колонки (col.name).
   */
  metricTemplateNames?: Record<string, string>;
  /**
   * Импортировать ли колонки-метрики БЕЗ пользовательского шаблона (каждую как
   * отдельный шаблон по имени колонки). false → только привязанные к шаблонам.
   * По умолчанию true (ничего не теряем).
   */
  importUnassignedMetrics?: boolean;
  /**
   * Спецификации шаблонов (формула + формат), заданные пользователем при импорте.
   * Ключ связи с колонками — имя (см. `metricTemplateNames`). Нет спеки для имени
   * → дефолт `SUM(value)`, формат `number`.
   */
  metricTemplateSpecs?: AggregateTemplateSpec[];
}

/** Стабильный ключ узла из пути. Разделитель U+0001 — чтобы ['a','b'] и
 *  ['ab'] не схлопывались в один ключ. */
export function nodePathKey(path: string[]): string {
  return path.join('');
}
